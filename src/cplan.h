//
// 🔴 Baked, not ported, and size isn't the reason. colorPlan() sorts with a predicate
// that isn't a strict weak order, so its ties fall to Swift's introsort internals —
// re-deriving that algorithm in C would yield a DIFFERENT, equally plausible C, and
// nothing would ever tell us it was wrong. The plan is a constant. The only faithful
// port of a constant is the constant.
//
// Same for the colours: CUBE_STATES is every state the dance passes through, so the
// engine needs no cube model, no move logic and no solver — it looks colours up.
//
