#define NGLYPH (int)(sizeof GLYPH / sizeof GLYPH[0])

void text_put(int x, int y, int s, char c, uint8_t ci) {
    if (c >= 'a' && c <= 'z') c = (char)(c - 'a' + 'A');
    for (int g = 0; g < NGLYPH; g++) {
        if (GLYPH[g].c != c) continue;
        for (int col = 0; col < 5; col++)
            for (int row = 0; row < 7; row++)
                if (GLYPH[g].col[col] & (1 << row))
                    for (int dy = 0; dy < s; dy++)
                        for (int dx = 0; dx < s; dx++) {
                            int px = x + col * s + dx, py = y + row * s + dy;
                            if (px >= 0 && px < g_fbw && py >= 0 && py < g_fbh)
                                g_fb[py * g_fbw + px] = ci;
                        }
        return;
    }
}

void text_draw(int x, int y, int s, const char *str, uint8_t ci) {
    for (const char *p = str; *p; p++, x += 6 * s) text_put(x, y, s, *p, ci);
}
int text_width(const char *s, int sc) { return (int)strlen(s) * 6 * sc - sc; }

